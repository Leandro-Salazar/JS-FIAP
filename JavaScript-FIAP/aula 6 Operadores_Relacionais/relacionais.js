let numero1 = 20;
let numero2 = 10;
let resultado;

console.log (`Primeiro Número: ${numero1}`);
console.log (`Segundo Número: ${numero2}`);

//Igualdade simples ==
resultado = numero1 == numero2;
console.log(`${numero1} == ${numero2} = ${resultado}`);

//Desigualdade !=
resultado = numero1 != numero2;
console.log(`${numero1} != ${numero2} = ${resultado}`);

//Igualdade estrita === (Boa Pratica)
resultado = numero1 === numero2;
console.log(`${numero1} === ${numero2} = ${resultado}`);

//Desigualdade estrita !== (Boa Pratica)
resultado = numero1 !== numero2;
console.log(`${numero1} !== ${numero2} = ${resultado}`);

//Maior >
resultado = numero1 > numero2;
console.log(`${numero1} > ${numero2} = ${resultado}`);

//Maior ou Igual >
resultado = numero1 >= numero2;
console.log(`${numero1} >= ${numero2} = ${resultado}`);

//Menor ou Igual >
resultado = numero1 <= numero2;
console.log(`${numero1} <= ${numero2} = ${resultado}`);

//Menor >
resultado = numero1 < numero2;
console.log(`${numero1} < ${numero2} = ${resultado}`);






