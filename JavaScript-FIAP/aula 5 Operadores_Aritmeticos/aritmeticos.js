let primeiroNumero = 50;
let segundoNumero = 10;
let resultado;

console.log(`Primeiro Número = ${primeiroNumero}`);
console.log(`Segundo Número = ${segundoNumero}`);

// Adição
resultado = primeiroNumero + segundoNumero;
console.log(`${primeiroNumero} + ${segundoNumero} = ${resultado}`)

//Subtração
resultado = primeiroNumero - segundoNumero;
console.log(`${primeiroNumero} - ${segundoNumero} = ${resultado}`)

//Divisão
resultado = primeiroNumero / segundoNumero;
console.log(`${primeiroNumero} / ${segundoNumero} = ${resultado}`)

//Multiplicação
resultado = primeiroNumero * segundoNumero;
console.log(`${primeiroNumero} * ${segundoNumero} = ${resultado}`)

//Módulo
resultado = primeiroNumero % segundoNumero;
console.log(`${primeiroNumero} % ${segundoNumero} = ${resultado}`)