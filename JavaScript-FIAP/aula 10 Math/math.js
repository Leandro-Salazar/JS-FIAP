let numero = 78.53;
console.log(numero);

//Math.ceil() - Arredonda para cima
console.log(`Math.ceil(${numero}) = ${Math.ceil(numero)})`);

//Math.floor() - Arredonda para baixo
console.log(`Math.floor(${numero}) = ${Math.floor(numero)})`);

//Math.round() - Arredonda 
console.log(`Math.round(${numero}) = ${Math.round(numero)})`);

//Math.max() - Puxa o Maior Númro
console.log(Math.max(1,5,200,3));

//Math.min() - Puxa o Menor Número
console.log(Math.min(1,5,200,3));

//ParseInt() - Puxa o Numero Inteiro
console.log(`parseInt(${numero}) = ${parseInt(numero)})`);

//Math.pow() - Potência
console.log(Math.pow(8,3));

//Math.random() - Numero Aleatorio entre 0 e 1
let numeroSorteado = Math.random();
console.log(numeroSorteado);

//Math.sqrt - Raiz Quadrada
console.log(Math.sqrt(25));

//Math.cbrt - Raiz Cúbica
console.log(Math.cbrt(27));
