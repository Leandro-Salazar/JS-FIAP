let primeiroNumero = 100;

console.log(`Primeiro Número: ${primeiroNumero}`);


//Adiciona
primeiroNumero+=10;
console.log(`Primeiro Número: ${primeiroNumero}`);

//Subtrai
primeiroNumero-=10;
console.log(`Primeiro Número: ${primeiroNumero}`);

//Multiplica
primeiroNumero*=10;
console.log(`Primeiro Número: ${primeiroNumero}`);

//Divide
primeiroNumero/=10;
console.log(`Primeiro Número: ${primeiroNumero}`);

//Módulo
primeiroNumero**10;
console.log(`Primeiro Número: ${primeiroNumero}`);

//Potencia
primeiroNumero%=10;
console.log(`Primeiro Número: ${primeiroNumero}`);





